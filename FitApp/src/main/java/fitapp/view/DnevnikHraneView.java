    private boolean postojiHranaUModelu(DefaultListModel<Hrana> m, Hrana h) {
        for (int i = 0; i < m.getSize(); i++) {
            if (m.get(i).getSifra().equals(h.getSifra())) {
                return true;
            }
        }
        return false;
    }


