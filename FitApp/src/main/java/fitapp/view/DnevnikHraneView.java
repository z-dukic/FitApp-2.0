    private void obrisiHranuIzDnevnika(Hrana p) {
        DefaultListModel<Hrana> m = (DefaultListModel<Hrana>) lstDnevnik.getModel();
        for (int i = 0; i < m.getSize(); i++) {
            if (m.get(i).getSifra().equals(p.getSifra())) {
                m.removeElementAt(i);
                return;
            }
        }

    }


