    public void ucitaj() {
        DefaultListModel<DnevnikHrane> m = new DefaultListModel<>();
        dnevnikHranaController.read().forEach(g -> {
            m.addElement(g);
        });
        lstDatuma.setModel(m);

    }

    public void ucitajDruguListu() {
        DefaultListModel<Hrana> z = new DefaultListModel<>();
        hranaController.read().forEach(q -> {
            z.addElement(q);
        });
        lstDnevnikHrane.setModel(z);
    }

    private void postavke() {
        setTitle(Aplikacija.NASLOV_APP + " Dnevnik");

        DatePickerSettings dps = new DatePickerSettings(new Locale("hr", "HR"));
        dps.setFormatForDatesCommonEra("dd.MM.yyyy");

        dpDatum.setSettings(dps);

        lstDnevnik.setModel(new DefaultListModel<Hrana>());

        lstDnevnik.setModel(new DefaultListModel<Hrana>());
        GregorianCalendar c = (GregorianCalendar) Calendar.getInstance();
        c.setTime(new Date());
        c.set(Calendar.HOUR_OF_DAY, 0);
        c.set(Calendar.MINUTE, 0);
        c.set(Calendar.SECOND, 0);
        c.set(Calendar.MILLISECOND, 0);
        trenutniDatum = c.getTime();

        lblTrenutniDatum.setText(df.format(trenutniDatum));
    }

    public void datum() {
        Date datum = new Date();
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        String datumIzbornik = sdf.format(datum);
        txtDatum.setText(datumIzbornik);

    }

    private void vrijeme() {
        Thread t1;
        t1 = new Thread(() -> {
            while (true) {
                Date datum = new Date();
                SimpleDateFormat sdf = new SimpleDateFormat("HH:mm:ss");
                txtVrijeme.setText(sdf.format(datum));
                try {
                    Thread.sleep(1000);
                } catch (InterruptedException ex) {
                    System.out.println("Error with clock update");
                }
            }
        });
        t1.start();

    }

    private void postaviVrijednostiUEntitet() {
        var e = dnevnikHranaController.getEntitet();

        if (dpDatum.getDate() != null) {
            e.setDatum(Date.from(dpDatum.getDate().atStartOfDay()
                    .atZone(ZoneId.systemDefault()).toInstant())
            );

        }

        DefaultListModel<Hrana> m = (DefaultListModel<Hrana>) lstDnevnik.getModel();
        List<Hrana> lista = new ArrayList<>();
        for (int i = 0; i < m.getSize(); i++) {
            lista.add(m.get(i));
        }
        e.setHrana(lista);

    }

